const int N = 1e6 + 7;
vector<bool> isPrime(N, true);
void seiveAlgo()
{
    isPrime[0] = isPrime[1] = false;
    for (int i = 2; i < N; i++)
    {
        if (isPrime[i] == true)
        {
            for (int j = 2 * i; j < N; j += i)
            {
                isPrime[j] = false;
            }
        }
    }
}
void solve()
{
    int n;
    cin >> n;
    char arr[n][n];
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            cin >> arr[i][j];
        }
    }

    vector<int> ans;
    for (int i = 0; i < n; i++)
    {
        int count = 0;
        for (int j = 0; j < n; j++)
        {
            if (arr[i][j] == '1')
            {
                count++;
            }
        }
        if (count != 0)
            ans.push_back(count);
        if (ans.size() >= 2)
            break;
    }

    for (int i = 0; i < ans.size(); i++)
    {
        if (ans[i] == ans[i + 1])
            cout << "SQUARE" << endl;
        else
            cout << "TRIANGLE" << endl;
        break;
    }
}

